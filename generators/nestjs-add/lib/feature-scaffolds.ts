import type { TemplateContext } from '../../lib/types';

function lines(...content: string[]): string {
  return `${content.join('\n')}\n`;
}

export const GRAPHQL_GUARD_DEPENDENCIES = [
  '@apollo/server',
  '@as-integrations/fastify',
  '@nestjs/apollo',
  '@nestjs/graphql',
  'graphql',
] as const;

export const GRAPHQL_MANAGED_PATHS = ['src/modules/graphql'] as const;

export function buildGraphqlFeatureFiles(
  context: TemplateContext,
): Record<string, string> {
  return {
    'src/modules/graphql/index.ts': lines(
      "export { GraphqlFeatureModule } from './graphql.module';",
    ),
    'src/modules/graphql/graphql.module.ts': lines(
      "import { Module } from '@nestjs/common';",
      '',
      "import { AuthModule } from '../auth/auth.module';",
      "import { GraphqlDemoResolver } from './graphql-demo.resolver';",
      "import { GqlOptionalAuthGuard } from './security/gql-optional-auth.guard';",
      '',
      '@Module({',
      '  imports: [AuthModule],',
      '  providers: [GraphqlDemoResolver, GqlOptionalAuthGuard],',
      '})',
      'export class GraphqlFeatureModule {}',
    ),
    'src/modules/graphql/graphql-demo.type.ts': lines(
      "import { Field, ObjectType } from '@nestjs/graphql';",
      '',
      '@ObjectType()',
      'export class GraphqlDemo {',
      '  @Field(() => String)',
      '  service!: string;',
      '',
      '  @Field(() => Boolean)',
      '  authenticated!: boolean;',
      '',
      '  @Field(() => String, { nullable: true })',
      '  guestUserId!: string | null;',
      '}',
    ),
    'src/modules/graphql/graphql-demo.resolver.ts': lines(
      "import { UseGuards } from '@nestjs/common';",
      "import { Query, Resolver } from '@nestjs/graphql';",
      '',
      "import { GraphqlDemo } from './graphql-demo.type';",
      "import {",
      "  CurrentGraphqlUser,",
      "  type GraphqlCurrentUser,",
      "} from './security/current-graphql-user.decorator';",
      "import { GqlOptionalAuthGuard } from './security/gql-optional-auth.guard';",
      '',
      '@Resolver(() => GraphqlDemo)',
      'export class GraphqlDemoResolver {',
      "  @Query(() => GraphqlDemo, { name: 'graphqlDemo' })",
      '  @UseGuards(GqlOptionalAuthGuard)',
      '  getGraphqlDemo(',
      '    @CurrentGraphqlUser() currentUser: GraphqlCurrentUser,',
      '  ): GraphqlDemo {',
      '    return {',
      `      service: ${JSON.stringify(context.appName)},`,
      '      authenticated: Boolean(currentUser.user?.sub),',
      '      guestUserId: currentUser.guestUserId,',
      '    };',
      '  }',
      '}',
    ),
    'src/modules/graphql/security/current-graphql-user.decorator.ts': lines(
      "import { ExecutionContext, createParamDecorator } from '@nestjs/common';",
      "import { GqlExecutionContext } from '@nestjs/graphql';",
      '',
      'export interface GraphqlCurrentUser {',
      '  user: { sub?: string } | null;',
      '  guestUserId: string | null;',
      '}',
      '',
      'export const CurrentGraphqlUser = createParamDecorator(',
      '  (_data: unknown, context: ExecutionContext): GraphqlCurrentUser => {',
      '    const gqlContext = GqlExecutionContext.create(context).getContext<{',
      '      req?: { user?: { sub?: string } | null };',
      '      guestUserId?: string | null;',
      '    }>();',
      '',
      '    return {',
      '      user: gqlContext.req?.user ?? null,',
      '      guestUserId: gqlContext.guestUserId ?? null,',
      '    };',
      '  },',
      ');',
    ),
    'src/modules/graphql/security/gql-optional-auth.guard.ts': lines(
      "import { ExecutionContext, Injectable } from '@nestjs/common';",
      "import { GqlExecutionContext } from '@nestjs/graphql';",
      "import { AuthGuard } from '@nestjs/passport';",
      '',
      '@Injectable()',
      "export class GqlOptionalAuthGuard extends AuthGuard('jwt') {",
      '  canActivate(context: ExecutionContext) {',
      '    const request = this.getRequest(context);',
      '    const authorization =',
      '      request?.headers?.authorization ?? request?.headers?.Authorization;',
      '    const hasBearer =',
      "      typeof authorization === 'string' &&",
      "      authorization.toLowerCase().startsWith('bearer ');",
      '',
      '    if (!hasBearer) {',
      '      return true;',
      '    }',
      '',
      '    return super.canActivate(context);',
      '  }',
      '',
      '  getRequest(context: ExecutionContext) {',
      '    const gqlContext = GqlExecutionContext.create(context).getContext<{',
      '      req?: { headers?: Record<string, unknown> };',
      '    }>();',
      '',
      '    return gqlContext.req;',
      '  }',
      '',
      '  handleRequest(err: unknown, user: unknown) {',
      '    if (err || !user) {',
      '      return null;',
      '    }',
      '',
      '    return user;',
      '  }',
      '}',
    ),
  };
}

