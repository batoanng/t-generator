export const CACHE_GUARD_DEPENDENCIES = [
  '@keyv/redis',
  '@nestjs/cache-manager',
  'cache-manager',
] as const;

export const CACHE_MANAGED_PATHS = ['src/modules/cache'] as const;

export function buildCacheFeatureFiles(): Record<string, string> {
  return {
    'src/modules/cache/index.ts': lines(
      "export { CacheFeatureModule } from './cache.module';",
      "export { CacheDemoService } from './cache.service';",
    ),
    'src/modules/cache/cache.schemas.ts': lines(
      "import { z } from 'zod';",
      '',
      'export const CacheDemoKeySchema = z.string().trim().min(1);',
      '',
      'export const CacheDemoRequestSchema = z.object({',
      '  key: CacheDemoKeySchema,',
      '  value: z.string().trim().min(1),',
      '  ttlMs: z.coerce.number().int().positive().max(86_400_000).optional(),',
      '});',
      '',
      'export type CacheDemoRequest = z.infer<typeof CacheDemoRequestSchema>;',
    ),
    'src/modules/cache/cache.service.ts': lines(
      "import { CACHE_MANAGER } from '@nestjs/cache-manager';",
      "import { Inject, Injectable } from '@nestjs/common';",
      "import type { Cache } from 'cache-manager';",
      '',
      "import type { CacheDemoRequest } from './cache.schemas';",
      '',
      '@Injectable()',
      'export class CacheDemoService {',
      '  constructor(@Inject(CACHE_MANAGER) private readonly cacheManager: Cache) {}',
      '',
      '  async setValue(request: CacheDemoRequest) {',
      '    const ttlMs = request.ttlMs ?? 60_000;',
      '',
      '    await this.cacheManager.set(request.key, request.value, ttlMs);',
      '',
      '    return {',
      '      key: request.key,',
      '      value: request.value,',
      '      ttlMs,',
      '    };',
      '  }',
      '',
      '  async getValue(key: string) {',
      '    return {',
      '      key,',
      '      value: (await this.cacheManager.get<string>(key)) ?? null,',
      '    };',
      '  }',
      '}',
    ),
    'src/modules/cache/cache.controller.ts': lines(
      "import { Body, Controller, Get, Param, Post } from '@nestjs/common';",
      '',
      "import {",
      "  CacheDemoKeySchema,",
      "  CacheDemoRequestSchema,",
      "} from './cache.schemas';",
      "import { CacheDemoService } from './cache.service';",
      '',
      "@Controller('cache')",
      'export class CacheController {',
      '  constructor(private readonly cacheDemo: CacheDemoService) {}',
      '',
      "  @Post('demo')",
      "  async setDemoValue(@Body() body: unknown) {",
      '    const request = CacheDemoRequestSchema.parse(body);',
      '',
      '    return this.cacheDemo.setValue(request);',
      '  }',
      '',
      "  @Get('demo/:key')",
      '  async getDemoValue(@Param(\'key\') key: string) {',
      '    const parsedKey = CacheDemoKeySchema.parse(key);',
      '',
      '    return this.cacheDemo.getValue(parsedKey);',
      '  }',
      '}',
    ),
    'src/modules/cache/cache.module.ts': lines(
      "import { Module } from '@nestjs/common';",
      '',
      "import { CacheController } from './cache.controller';",
      "import { CacheDemoService } from './cache.service';",
      '',
      '@Module({',
      '  controllers: [CacheController],',
      '  providers: [CacheDemoService],',
      '  exports: [CacheDemoService],',
      '})',
      'export class CacheFeatureModule {}',
    ),
  };
}

export const LLM_GUARD_DEPENDENCIES = ['openai'] as const;

export const LLM_MANAGED_PATHS = ['src/modules/llm'] as const;

export function buildLlmFeatureFiles(): Record<string, string> {
  return {
    'src/modules/llm/index.ts': lines(
      "export { LlmFeatureModule } from './llm.module';",
    ),
    'src/modules/llm/llm.schemas.ts': lines(
      "import { z } from 'zod';",
      '',
      'export const LlmDemoRequestSchema = z.object({',
      '  prompt: z.string().trim().min(1),',
      "  provider: z.enum(['openai']).default('openai'),",
      '});',
      '',
      'export type LlmDemoRequest = z.infer<typeof LlmDemoRequestSchema>;',
    ),
    'src/modules/llm/openai.provider.ts': lines(
      "import OpenAI from 'openai';",
      '',
      "import type { Config } from '../../types/config';",
      "import { Service } from '../tokens';",
      '',
      "export const OPENAI_CLIENT = Symbol('OPENAI_CLIENT');",
      '',
      'export const openAiClientProvider = {',
      '  provide: OPENAI_CLIENT,',
      '  inject: [Service.CONFIG],',
      '  useFactory: (config: Config): OpenAI =>',
      '    new OpenAI({',
      '      apiKey: config.OPENAI_API_KEY,',
      '    }),',
      '};',
    ),
    'src/modules/llm/llm.service.ts': lines(
      "import { Inject, Injectable } from '@nestjs/common';",
      "import OpenAI from 'openai';",
      '',
      "import type { Config } from '../../types/config';",
      "import { Service } from '../tokens';",
      "import type { LlmDemoRequest } from './llm.schemas';",
      "import { OPENAI_CLIENT } from './openai.provider';",
      '',
      '@Injectable()',
      'export class LlmService {',
      '  constructor(',
      '    @Inject(OPENAI_CLIENT) private readonly openAi: OpenAI,',
      '    @Inject(Service.CONFIG) private readonly config: Config,',
      '  ) {}',
      '',
      '  async runPromptChain(request: LlmDemoRequest) {',
      '    const completion = await this.openAi.chat.completions.create({',
      '      model: this.config.OPENAI_MODEL,',
      '      temperature: 0,',
      '      messages: [',
      '        {',
      "          role: 'system',",
      "          content: 'You are the concise LLM demo for a generated NestJS server.',",
      '        },',
      '        {',
      "          role: 'user',",
      '          content:',
      "            `Summarize the intent of the following prompt in one short paragraph.\\n\\n${request.prompt}`,",
      '        },',
      '      ],',
      '    });',
      '',
      '    const output =',
      '      completion.choices.find(',
      '        (choice) =>',
      "          typeof choice.message.content === 'string' &&",
      '          choice.message.content.trim().length > 0,',
      '      )?.message.content?.trim() ?? \'\';',
      '',
      '    return {',
      '      provider: request.provider,',
      '      model: this.config.OPENAI_MODEL,',
      '      output,',
      '    };',
      '  }',
      '}',
    ),
    'src/modules/llm/llm.controller.ts': lines(
      "import { Body, Controller, Post } from '@nestjs/common';",
      '',
      "import { LlmDemoRequestSchema } from './llm.schemas';",
      "import { LlmService } from './llm.service';",
      '',
      "@Controller('llm')",
      'export class LlmController {',
      '  constructor(private readonly llm: LlmService) {}',
      '',
      "  @Post('demo')",
      '  async runDemo(@Body() body: unknown) {',
      '    const request = LlmDemoRequestSchema.parse(body);',
      '',
      '    return this.llm.runPromptChain(request);',
      '  }',
      '}',
    ),
    'src/modules/llm/llm.module.ts': lines(
      "import { Module } from '@nestjs/common';",
      '',
      "import { CommonModule } from '../common';",
      "import { LlmController } from './llm.controller';",
      "import { LlmService } from './llm.service';",
      "import { openAiClientProvider } from './openai.provider';",
      '',
      '@Module({',
      '  imports: [CommonModule],',
      '  controllers: [LlmController],',
      '  providers: [openAiClientProvider, LlmService],',
      '  exports: [LlmService],',
      '})',
      'export class LlmFeatureModule {}',
    ),
  };
}
